function error = objective_alpha(alpha)
    % داده‌های ورودی (به عنوان مثال، وضعیت هدف و وضعیت فعلی)
    theta = [0; 0]; % وضعیت اولیه
    dtheta = [0; 0]; % سرعت اولیه
    e = [1; 1]; % خطا
    de = [0; 0]; % تغییر خطا
    ddthetad = [0; 0]; % شتاب هدف

    % مقدار U با استفاده از تابع کنترل شما
    [U, ~] = fcn(alpha, theta, dtheta, e, de, ddthetad);
    
    % محاسبه خطا یا هزینه (این تابع باید متناسب با نیاز شما باشد)
    error = norm(U); % به عنوان مثال، می‌توانید نرمال U را محاسبه کنید
end